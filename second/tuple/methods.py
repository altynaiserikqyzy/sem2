tuple1 = (10, 20, 30, 20, 40, 50, 20)
# Using count() to count occurrences of 20
count_20 = tuple1.count(20)
print(f"The value 20 appears {count_20} times in tuple1.")
# Using index() to find the first occurrence of 30
index_30 = tuple1.index(30)
print(f"The value 30 is found at index {index_30} in tuple1.")
