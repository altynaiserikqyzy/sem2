# Create a dictionary with key-value pairs
my_dict = {
    "name": "Altynai",
    "age": 18,
    "city": "Semey"
}

# Access a value by its key
print(my_dict["name"])  # Output: Altynai

# Add a new key-value pair
my_dict["hobby"] = "sleeping"
print(my_dict)  # Output: {'name': 'Altynai', 'age': 18, 'city': 'Semey', 'hobby': 'sleeping'}

# Update an existing value
my_dict["age"] = 17
print(my_dict["age"])  # Output: 17